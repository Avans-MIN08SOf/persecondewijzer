package logic;

/**
 * @author nanne
 *
 */
public class Highscore {
	private String spelerNaam;
	private String score;
	private String tijdOver;
	/**
	 * @return the spelerNaam
	 */
	public String getSpelerNaam() {
		return spelerNaam;
	}
	/**
	 * @param spelerNaam the spelerNaam to set
	 */
	public void setSpelerNaam(String spelerNaam) {
		this.spelerNaam = spelerNaam;
	}
	/**
	 * @return the score
	 */
	public String getScore() {
		return score;
	}
	/**
	 * @param score the score to set
	 */
	public void setScore(String score) {
		this.score = score;
	}
	/**
	 * @return the tijdOver
	 */
	public String getTijdOver() {
		return tijdOver;
	}
	/**
	 * @param tijdOver the tijdOver to set
	 */
	public void setTijdOver(String tijdOver) {
		this.tijdOver = tijdOver;
	}
}
